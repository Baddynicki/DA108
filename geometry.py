#calculate_area_rectangle(length, width): Calculates and returns the area of a rectangle.
def calculate_area_rectangle(length, width):
    return(length * width)
#calculate_area_circle(radius): Calculates and returns the area of a circle.
def calculate_area_circle(radius):
    return(3.14 * radius * radius)
#calculate_perimeter_rectangle(length, width): Calculates and returns the perimeter of a rectangle.
def calculate_perimeter_rectangle(length, width):
    return(2 * (length + width))
#calculate_circumference_circle(radius): Calculates and returns the circumference of a circle.
def calculate_circumference_circle(radius):
    return(2 * 3.14 * radius)
